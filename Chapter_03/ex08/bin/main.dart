void main() {
  var b = true;
  if (b) {
    print('True'); //will print: True
  }
  var a = 1;
  var c = 2;

  if (a == c) {
    print('Equal To');
  }

  if (a != c) {
    print('Not Equal To');
  }

  if (a > c) {
    print('Greater Than');
  }

  if (a < c) {
    print('Less Than');
  }

  if (a >= c) {
    print('Greater Than or Equal To');
  }

  if (a <= c) {
    print('Less Than or Equal To');
  }
}
